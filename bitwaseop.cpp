//c++ program to illustrate Bitwise AND,OR,XOR operators
#include <iostream>
using namespace std;
int main() {
    int a = 10, b = 28;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a & b = " << (a & b) << endl;
    cout << "a | b = " << (a | b) << endl;
    cout << "a ^ b = " << (a ^ b) << endl;
    return 0;
}

